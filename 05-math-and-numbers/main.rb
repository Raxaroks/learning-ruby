# frozen_string_literal: true

# Lesson 05 - Math and numbers

puts 'Module'
remain = 10 % 3
puts remain
print "\n"

puts 'Decimal round'
num = 3.1416
puts num.floor
print "\n"

puts 'Square root'
puts Math.sqrt(16)
print "\n"

puts 'Logarithm'
puts Math.log(1)
print "\n"

puts 'Remeber, divide through certain type restricts the results'
puts "[integers] 7/5 = #{7 / 2}"
puts "[float and integer] 7.0/5 = #{7.0 / 2}"
print "\n"
